public class BonusAndSaturday {

    private double bonus;
    private int numbersOfSaturdays;

    public BonusAndSaturday() {
    }

    public double getBonus() {
        return bonus;
    }

    public void setBonus(double bonus) {
        this.bonus = bonus;
    }

    public int getNumbersOfSaturdays() {
        return numbersOfSaturdays;
    }

    public void setNumbersOfSaturdays(int numbersOfSaturdays) {
        this.numbersOfSaturdays = numbersOfSaturdays;
    }

}